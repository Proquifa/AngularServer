package com.proquifa.net.modelo.comun.facturaElectronica.addenda.Darier;

public class AddendaDarierDescuentoRecargo {
	private String operacion;
	private String imputacion;
	private String servicio;
	private String descripcion;
	private String base;
	private String tasa;
	private String monto;
	
	public String getOperacion() {
		return operacion;
	}
	
	public void setOperacion(String operacion) {
		this.operacion = operacion;
	}
	
	public String getImputacion() {
		return imputacion;
	}
	
	public void setImputacion(String imputacion) {
		this.imputacion = imputacion;
	}
	
	public String getServicio() {
		return servicio;
	}
	
	public void setServicio(String servicio) {
		this.servicio = servicio;
	}
	
	public String getDescripcion() {
		return descripcion;
	}
	
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	public String getBase() {
		return base;
	}
	
	public void setBase(String base) {
		this.base = base;
	}
	
	public String getTasa() {
		return tasa;
	}
	
	public void setTasa(String tasa) {
		this.tasa = tasa;
	}
	
	public String getMonto() {
		return monto;
	}
	
	public void setMonto(String monto) {
		this.monto = monto;
	}

}
