package com.proquifa.net.modelo.comun;

/**
 * @author bryan.magana
 *
 */
public class Modificacion {

	private long idEmpleado;
	private String ventana;
	private String servicio;
	private String metodo;
	private String modificaciones;
	/**
	 * @return the modificaciones
	 */
	public String getModificaciones() {
		return modificaciones;
	}
	/**
	 * @param modificaciones the modificaciones to set
	 */
	public void setModificaciones(String modificaciones) {
		this.modificaciones = modificaciones;
	}
	/**
	 * @return the ventana
	 */
	public String getVentana() {
		return ventana;
	}
	/**
	 * @param ventana the ventana to set
	 */
	public void setVentana(String ventana) {
		this.ventana = ventana;
	}
	/**
	 * @return the servicio
	 */
	public String getServicio() {
		return servicio;
	}
	/**
	 * @param servicio the servicio to set
	 */
	public void setServicio(String servicio) {
		this.servicio = servicio;
	}
	/**
	 * @return the metodo
	 */
	public String getMetodo() {
		return metodo;
	}
	/**
	 * @param metodo the metodo to set
	 */
	public void setMetodo(String metodo) {
		this.metodo = metodo;
	}
	/**
	 * @return the idEmpleado
	 */
	public long getIdEmpleado() {
		return idEmpleado;
	}
	/**
	 * @param idEmpleado the idEmpleado to set
	 */
	public void setIdEmpleado(long idEmpleado) {
		this.idEmpleado = idEmpleado;
	}
	
	
}
