package com.proquifa.net.modelo.comun.facturaElectronica.addenda.Darier;

public class AddendaDarierInfoAduanera {
	private String numeroDePedimento;
	private String fechaDePedimento;
	private String nombreDeAduana;
	
	public String getNumeroDePedimento() {
		return numeroDePedimento;
	}
	
	public void setNumeroDePedimento(String numeroDePedimento) {
		this.numeroDePedimento = numeroDePedimento;
	}
	
	public String getFechaDePedimento() {
		return fechaDePedimento;
	}
	
	public void setFechaDePedimento(String fechaDePedimento) {
		this.fechaDePedimento = fechaDePedimento;
	}
	
	public String getNombreDeAduana() {
		return nombreDeAduana;
	}
	
	public void setNombreDeAduana(String nombreDeAduana) {
		this.nombreDeAduana = nombreDeAduana;
	}

	
}
