package com.proquifa.net.modelo.comun;

public class ValorCombo {

	private Long idValorCombo;
	private String concepto;
	private String valor;
	private String tipo;
	/**
	 * @return the idValorCombo
	 */
	public Long getIdValorCombo() {
		return idValorCombo;
	}
	/**
	 * @param idValorCombo the idValorCombo to set
	 */
	public void setIdValorCombo(Long idValorCombo) {
		this.idValorCombo = idValorCombo;
	}
	/**
	 * @return the concepto
	 */
	public String getConcepto() {
		return concepto;
	}
	/**
	 * @param concepto the concepto to set
	 */
	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}
	/**
	 * @return the valor
	 */
	public String getValor() {
		return valor;
	}
	/**
	 * @param valor the valor to set
	 */
	public void setValor(String valor) {
		this.valor = valor;
	}
	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}


}
